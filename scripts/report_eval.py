#!/usr/bin/env python3
"""
Generate evaluation report for PR comments.

Reads evaluation config files generated by test_eval.py, queries LangSmith
for results, and outputs a markdown summary suitable for PR comments.

Usage:
    python scripts/report_eval.py
    python scripts/report_eval.py evaluation_config__*.json
    python scripts/report_eval.py --output custom_report.md
"""

import argparse
import glob
import json
import operator
import os
import sys
from collections import defaultdict

from dotenv import load_dotenv
load_dotenv()

from langsmith import Client

# Operator map for threshold comparisons
OP_MAP = {
    ">": operator.gt,
    "<": operator.lt,
    ">=": operator.ge,
    "<=": operator.le,
    "==": operator.eq,
    "!=": operator.ne,
}


def parse_threshold(threshold_str: str) -> tuple:
    """Parse threshold expression like '>=0.75' into (operator, value)."""
    for symbol in sorted(OP_MAP.keys(), key=len, reverse=True):
        if threshold_str.startswith(symbol):
            return OP_MAP[symbol], float(threshold_str[len(symbol):])
    raise ValueError(f"Invalid threshold format: {threshold_str}")


def process_config(config_path: str, client: Client) -> dict:
    """Process a single evaluation config and fetch results from LangSmith."""
    print(f"Processing: {config_path}")

    with open(config_path) as f:
        config = json.load(f)

    experiment_name = config.get("experiment_name")
    criteria = config.get("criteria", {})

    if not experiment_name:
        return {"error": f"No experiment_name in {config_path}"}

    # Fetch runs from LangSmith
    runs = list(client.list_runs(project_name=experiment_name))
    if not runs:
        return {"experiment_name": experiment_name, "error": "No runs found"}

    # Fetch feedback scores
    run_ids = [r.id for r in runs]
    feedbacks = client.list_feedback(run_ids=run_ids)

    feedback_by_key = defaultdict(list)
    for fb in feedbacks:
        if fb.score is not None:
            feedback_by_key[fb.key].append(fb.score)

    # Build results table
    table_rows = []
    num_passed = 0
    num_failed = 0

    for key, scores in feedback_by_key.items():
        avg_score = sum(scores) / len(scores) if scores else None
        threshold_expr = criteria.get(key)

        if threshold_expr and avg_score is not None:
            op, value = parse_threshold(threshold_expr)
            passed = op(avg_score, value)
            status = "PASS" if passed else "FAIL"
            if passed:
                num_passed += 1
            else:
                num_failed += 1
        else:
            status = "N/A"

        table_rows.append({
            "key": key,
            "score": f"{avg_score:.2f}" if avg_score else "N/A",
            "threshold": threshold_expr or "-",
            "status": status,
        })

    return {
        "experiment_name": experiment_name,
        "table_rows": table_rows,
        "num_passed": num_passed,
        "num_failed": num_failed,
    }


def write_markdown_report(results: list, output_file: str = "eval_comment.md"):
    """Write evaluation results to markdown file."""
    with open(output_file, "w") as f:
        f.write("# LangSmith Evaluation Results\n\n")

        for result in results:
            experiment_name = result.get("experiment_name", "Unknown")

            if "error" in result:
                f.write(f"### {experiment_name}\n\n")
                f.write(f"**Error:** {result['error']}\n\n")
                continue

            f.write(f"### {experiment_name}\n\n")
            f.write("| Metric | Score | Threshold | Status |\n")
            f.write("|--------|-------|-----------|--------|\n")

            for row in result["table_rows"]:
                status_icon = {"PASS": "PASS", "FAIL": "FAIL", "N/A": "-"}[row["status"]]
                f.write(f"| {row['key']} | {row['score']} | {row['threshold']} | {status_icon} |\n")

            num_passed = result.get("num_passed", 0)
            num_failed = result.get("num_failed", 0)
            f.write(f"\n**Summary:** {num_passed} passed, {num_failed} failed\n\n")

    print(f"Report written to {output_file}")


def main():
    parser = argparse.ArgumentParser(description="Generate LangSmith evaluation report")
    parser.add_argument("config_files", nargs="*", help="Config files (default: evaluation_config__*.json)")
    parser.add_argument("--output", "-o", default="eval_comment.md", help="Output file")
    args = parser.parse_args()

    # Initialize LangSmith client
    client = Client()

    # Find config files
    config_files = args.config_files or glob.glob("evaluation_config__*.json")
    if not config_files:
        print("No evaluation config files found")
        sys.exit(1)

    # Process each config
    results = []
    for config_path in config_files:
        if os.path.exists(config_path):
            results.append(process_config(config_path, client))

    if not results:
        print("No results to report")
        sys.exit(1)

    # Write report
    write_markdown_report(results, args.output)


if __name__ == "__main__":
    main()
